package com.polidea.rxandroidble2;

/**
 * Coding to be used when transmitting on the LE Coded PHY.
 */
public enum RxBlePhyOption {
    /**
     * No preferred coding.
     */
    PHY_OPTION_NO_PREFERRED,

    /**
     * Prefer the S=2 coding.
     */
    PHY_OPTION_S2,

    /**
     * Prefer the S=2 coding.
     */
    PHY_OPTION_S8
}
